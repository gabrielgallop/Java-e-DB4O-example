    private void jBTInsertFone1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTInsertFone1ActionPerformed
        Exame exame = new Exame(listaExames.size(), jTextField13.getText(), jTextField14.getText(), jTextField15.getText());

        if (isAlteraExame) {
            listaExames.add(id, exame);
            isAlteraExame = false;

        } else {
            listaExames.add(exame);
        }
        AtualizarListTabela(listaExames);
        habilitarPanel(false);
    }//GEN-LAST:event_jBTInsertFone1ActionPerformed


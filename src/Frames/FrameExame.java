    private void jBTRemover1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTRemover1ActionPerformed
        try {
            if (tabela1.getSelectedRow() < 0) {
                JOptionPane.showMessageDialog(this, "Selecione um item a ser excluido!!!", "Excluir item", 1);
            } else if (JOptionPane.showConfirmDialog(this, "Tem certeza que deseja apagar esse lançamento?", "Excluir Lançamento", JOptionPane.INFORMATION_MESSAGE) == JOptionPane.OK_OPTION) {
                int id = tabela1.getSelectedRow();
                listaExames.remove(id);
                AtualizarListTabela(listaExames);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro no Excluir: " + e.getMessage(), "Exame", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBTRemover1ActionPerformed

    private void jBTAlterar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTAlterar1ActionPerformed
        try {
            if (tabela1.getSelectedRow() < 0) {
                JOptionPane.showMessageDialog(this, "Selecione um item a ser editado!!!", "Manuteção Lançamento", 1);
            } else {
                habilitarPanel(true);
                id = tabela1.getSelectedRow();
                isAlteraExame = true;
                Exame exame = listaExames.get(id);
                jTextField13.setText(exame.getNome());
                jTextField14.setText(exame.getDescricao());
                jTextField15.setText(exame.getIndexReferencia());
                listaExames.remove(id);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro no Excluir: " + e.getMessage(), "Fone", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBTAlterar1ActionPerformed


    public void AtualizarListTabela(List<Exame> lista) {
        try {

            tabela1.getColumnModel().getColumn(0).setPreferredWidth(50);
            tabela1.getColumnModel().getColumn(1).setPreferredWidth(50);
            tabela1.getColumnModel().getColumn(2).setPreferredWidth(50);
            setlistaExames(lista);
            DefaultTableModel modelo = (DefaultTableModel) tabela1.getModel();
            modelo.setNumRows(0);
            for (Exame item : lista) {
                modelo.addRow(new Object[]{
                    item.getNome(), item.getDescricao(), item.getIndexReferencia()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao  AtualizarListTabela!!!");
        }
    }

    private void verificaAlteracao() {
        if (frame.isIsAlteraSolicitacao()) {
            setlistaExames(frame.getListaExame());
            AtualizarListTabela(listaExames);
        }
    }


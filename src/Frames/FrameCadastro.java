    private void HabilitarPanelFone(boolean status) {

        jPanel3.setEnabled(status);
        jPanel3.setVisible(status);
        jBTInsertFone.setVisible(status);
        jTextField9.setText("");
        jTextField10.setText("");
        jTextField11.setText("");
        jTextField12.setText("");
        jTextField13.setText("");
    }

    public void AtualizarListTabela(List<Fone> lista) {
        try {

            tabela.getColumnModel().getColumn(0).setPreferredWidth(50);
            tabela.getColumnModel().getColumn(1).setPreferredWidth(50);
            tabela.getColumnModel().getColumn(2).setPreferredWidth(100);
            tabela.getColumnModel().getColumn(3).setPreferredWidth(50);
            tabela.getColumnModel().getColumn(4).setPreferredWidth(50);
            setList(lista);
            DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
            modelo.setNumRows(0);
            for (Fone item : lista) {
                modelo.addRow(new Object[]{
                    item.getDdd(), item.getPrefixo(), item.getNumero(), item.getRamal(), item.getTipo()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao  AtualizarListTabela!!!");
        }
    }

    private void InserirFone() {
        Fone fone = new Fone(jTextField9.getText(), jTextField10.getText(), jTextField11.getText(), jTextField12.getText(), jTextField13.getText());
        if (isAlteraFone) {
            list.add(id, fone);
            isAlteraFone = false;
        } else {
            list.add(fone);
        }
        HabilitarPanelFone(false);
        AtualizarListTabela(list);
    }

    private void DeletarFone() {
        try {
            if (tabela.getSelectedRow() < 0) {
                JOptionPane.showMessageDialog(this, "Selecione um item a ser excluido!!!", "Excluir item", 1);
            } else if (JOptionPane.showConfirmDialog(this, "Tem certeza que deseja apagar esse lançamento?", "Excluir Lançamento", JOptionPane.INFORMATION_MESSAGE) == JOptionPane.OK_OPTION) {
                int id = tabela.getSelectedRow();
                list.remove(id);
                AtualizarListTabela(list);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro no Excluir: " + e.getMessage(), "Fone", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void AlterarFone() {
        try {
            if (tabela.getSelectedRow() < 0) {
                JOptionPane.showMessageDialog(this, "Selecione um item a ser editado!!!", "Manuteção Lançamento", 1);
            } else {
                HabilitarPanelFone(true);
                id = tabela.getSelectedRow();
                isAlteraFone = true;
                Fone fone = list.get(id);
                jTextField9.setText(fone.getDdd());
                jTextField10.setText(fone.getPrefixo());
                jTextField11.setText(fone.getNumero());
                jTextField12.setText(fone.getRamal());
                jTextField13.setText(fone.getTipo());
                list.remove(id);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro no Excluir: " + e.getMessage(), "Fone", JOptionPane.ERROR_MESSAGE);
        }

    }


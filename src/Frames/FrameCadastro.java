    private void jBTCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTCadastrarActionPerformed
        if (isMedico) {
            Medico medico = CriaClasseMedico();
            if (isManutencao) {
                jTextField14.setVisible(false);
                medicoManager.edit(medico, idMedico);
            } else if (medico.getCpf().isEmpty()) {
                JOptionPane.showMessageDialog(null, "CPF não pode ser em Branco!");
            } else {
                medicoManager.create(medico);
            }
            FrameList frame = new FrameList(true);
            frame.show(true);
        } else {
            Paciente paciente = CriaClassePaciente();
            if (isManutencao) {
                jTextField14.setVisible(false);
                pacienteManager.edit(paciente, idPaciente);
            } else if (paciente.getCpf().isEmpty()) {
                JOptionPane.showMessageDialog(null, "CPF não pode ser em Branco!");
            } else {
                pacienteManager.create(paciente);
            }
            FrameList frame = new FrameList(false);
            frame.show(true);
        }

        dispose();

    }//GEN-LAST:event_jBTCadastrarActionPerformed


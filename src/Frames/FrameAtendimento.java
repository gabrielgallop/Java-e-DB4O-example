    private void GravarAnamnese() {
        Anamnese anamnese = new Anamnese(jTextField10.getText(), jTextField11.getText());
        if (isAlteraAnamnese) {
            listaAnmAnamneses.add(id, anamnese);
            isAlteraAnamnese = false;
        } else {
            listaAnmAnamneses.add(anamnese);
        }
        HabilitarPanelProntuario(false);
        AtualizarListTabelaProntuario(listaAnmAnamneses);
    }

    private void GravarSolicitacao() {
        Solicita solicita = new Solicita();
        solicita.setListaExame(auxSolicita.getListaExame());
        solicita.setData(jXDData2.getDate());
        solicita.setLaboratorio(jTextField13.getText());
        solicita.setObservacao(jTextField14.getText());
        solicita.setResultado(jTextField15.getText());
        if (isAlteraSolicitacao) {
            listaSolicitas.add(id, solicita);
            isAlteraSolicitacao = false;

        } else {
            listaSolicitas.add(solicita);
        }
        AtualizarListTabelaSolicitacao(listaSolicitas);
        HabilitarPanelSolicita(false);

    }

    private void RemoverAnamnese() {
        try {
            if (tabela.getSelectedRow() < 0) {
                JOptionPane.showMessageDialog(this, "Selecione um item a ser excluido!!!", "Excluir item", 1);
            } else if (JOptionPane.showConfirmDialog(this, "Tem certeza que deseja apagar esse lançamento?", "Excluir Lançamento", JOptionPane.INFORMATION_MESSAGE) == JOptionPane.OK_OPTION) {
                int id = tabela.getSelectedRow();
                listaAnmAnamneses.remove(id);
                AtualizarListTabelaProntuario(listaAnmAnamneses);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro no Excluir: " + e.getMessage(), "Fone", JOptionPane.ERROR_MESSAGE);
        }
    }


    public void AtualizarListTabelaProntuario(List<Anamnese> lista) {
        try {

            tabela.getColumnModel().getColumn(0).setPreferredWidth(50);
            tabela.getColumnModel().getColumn(1).setPreferredWidth(50);
            setListaAnmAnamneses(lista);
            DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
            modelo.setNumRows(0);
            for (Anamnese item : lista) {
                modelo.addRow(new Object[]{
                    item.getPergunta(), item.getResposta()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao  AtualizarListTabelaProntuario!!!");
        }
    }

    public void AtualizarListTabelaSolicitacao(List<Solicita> lista) {
        try {

            tabela1.getColumnModel().getColumn(0).setPreferredWidth(50);
            tabela1.getColumnModel().getColumn(1).setPreferredWidth(50);
            tabela1.getColumnModel().getColumn(2).setPreferredWidth(50);
            tabela1.getColumnModel().getColumn(3).setPreferredWidth(50);
            setListaSolicitas(lista);
            DefaultTableModel modelo = (DefaultTableModel) tabela1.getModel();
            modelo.setNumRows(0);
            for (Solicita item : lista) {
                modelo.addRow(new Object[]{
                    item.getData(), item.getLaboratorio(), item.getObservacao(), item.getResultado()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao  AtualizarListTabelaSolicitacao!!!");
        }
    }

